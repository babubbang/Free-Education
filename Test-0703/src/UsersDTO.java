
public class UsersDTO {
	String 성명;
	String ID;
	String PW;
	String 전화번호;
	
	public String get성명() {
		return 성명;
	}
	public void set성명(String 성명) {
		this.성명 = 성명;
	}
	public String getID() {
		return ID;
	}
	public void setID(String ID) {
		this.ID = ID;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String PW) {
		this.PW = PW;
	}
	public String get전화번호() {
		return 전화번호;
	}
	public void set전화번호() {
		this.전화번호 = 전화번호;
	}
}
